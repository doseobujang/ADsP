# 피어슨 상관분석(선형적 상관관계) ------------------------------------------------------

X <- c( 1, 2, 3, 4, 5 )
Y <- c( 3, 6, 4, 9, 8 )
cor( X, Y, method = 'pearson' )


# 스피어만 상관분석(비선형적 상관관계) ----------------------------------------------------

X <- c( 1, 2, 3, 4, 5 )
Y <- c( 3, 6, 4, 9, 8 )
cor( X, Y, method = 'spearman' )


# 상관분석 실습 -----------------------------------------------------------------

time <- c( 8, 6, 7, 3, 2, 4, 2, 7, 2, 3 )
score <- c( 33, 22, 18, 6, 23, 10, 9, 30, 11, 13 )
